
public class LinkedList {
	
	private Person p;
	private int key;
	private LinkedList next;
	
	public LinkedList(Person p)
	{
		this.p = p;
		this.key = key;
		this.next = null;
	}
	public Person getP() {
		return p;
	}
	public void setP(Person p) {
		this.p = p;
	}
	public int getKey() {
		return key;
	}
	public void setKey(int key) {
		this.key = key;
	}
	public LinkedList getNext() {
		return next;
	}
	public void setNext(LinkedList next) {
		this.next = next;
	}
	
	

}


